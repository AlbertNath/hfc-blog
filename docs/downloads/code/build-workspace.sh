YELLOW="\033[38;2;255;215;0m"
GRAY="\033[38;2;176;179;184m" # #B0B3B8
RESET='\033[0m'

info() {
  echo -e "${GRAY}[!] $1${RESET}"
}

success() {
  echo -e "${GREEN}[✔] $1${RESET}"
}

warn() {
  echo -e "${YELLOW}[⚠] $1${RESET}"
}

error() {
  echo -e "${RED}[✘] $1${RESET}"
}

main() {
